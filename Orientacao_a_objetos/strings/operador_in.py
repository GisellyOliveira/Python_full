# Operador in

programacao_python = "Programação em Python"

# checa se a string contém "Python"
print("Python" in programacao_python) # Saída: True

# checa se a string contém "python"
print("Python" in programacao_python) # Saída: False

# checa se a string contém "abacate"
print("abacate" in programacao_python) # Saída: False