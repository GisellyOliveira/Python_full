# Indexação e Substrings

frase = "Python é muito divertido!"

# A indexação começa com 0
print(frase[3]) # imprimi a letra no índice 3

# Use -1 para pegar a última letra
print(frase[-1])

# Use -2 para pegar a penúltima letra
print(frase[-2])

# string[start:end] - end não é incluído
print(frase[:4]) # Imprimi da primeira letra até o índice 3
print(frase[10:]) # Imprimi do índice 10 até o final
print(frase[5:10]) # Imprimi do índice 5 até o índice 9
print(frase[:]) # Imprimi do primeiro ao último índice